/** Normalize catalog entries so every item has `image` + `images`. */
export const SHOP_ITEMS = RAW_SHOP_ITEMS.map((item) => {
  const images = Array.isArray(item.images)
    ? item.images.filter(Boolean)
    : item.image
      ? [item.image]
      : [];
  return {
    ...item,
    images,
    image: images[0] ?? null,
  };
});

export function findShopItem(id) {
  return SHOP_ITEMS.find((item) => item.id === id) ?? null;
}

/** Category order for custom-order dropdown (excludes "All"). */
export const SHOP_CATEGORY_ORDER = SHOP_CATEGORIES.filter((c) => c !== "All");

/** Grouped catalog for `<optgroup>` dropdowns. */
export function getShopItemGroups() {
  return SHOP_CATEGORY_ORDER.map((category) => ({
    category,
    items: SHOP_ITEMS.filter((item) => item.category === category),
  })).filter((group) => group.items.length > 0);
}
